SELECT 
CD_CLI AS CODIGO,
RZ_CLI AS NOME,
CGC_CPF AS CGC_CPF,
INSC_EST AS INSC_EST,
CASE	WHEN PESSOA = 0 
			THEN 'F'
			ELSE 'J' 
			END AS TIPO,
CASE	
		WHEN CID.UF = 'SN'  
		THEN 'MG'
		ELSE CID.UF
		END	AS UF
FROM RC_CLI CLI
INNER JOIN 
	V_GLB_CID_IPDV CID
	ON	CLI.CD_CID = CID.CD_CID
WHERE 
	1 = 1 


