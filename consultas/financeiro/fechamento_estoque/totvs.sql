SELECT 
	ZZ1010.ZZ1_FILLIN 										AS FILIAL,
	COUNT(B9_COD) 										AS QTDE_ITENS,
	--SUM(B9_QINI * ROUND(B9_CUSTD, 2)) 		AS CUSTO_CALCULADO,
	SUM(B9_VINI1) 											AS CUSTO,
	B9_DATA 														AS DT_REF
FROM 
	SB9010 
	INNER JOIN ZZ1010 ON 
	SB9010.B9_FILIAL = ZZ1010.ZZ1_FILPRO
WHERE 
	SB9010.D_E_L_E_T_ = ''
	AND ZZ1010.D_E_L_E_T_ = ''
	AND B9_QINI > 0
	AND B9_DATA = '20230430'
GROUP BY 
	ZZ1010.ZZ1_FILLIN,
	B9_DATA