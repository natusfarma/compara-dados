SELECT 
	ZZ1010.ZZ1_FILLIN												AS FILIAL,
	SE2010.E2_NUM													AS TITULO,
	SE2010.E2_FORNECE											AS CODFORN,
	SE2010.E2_NOMFOR											AS NMFORN,
	CONVERT(DATE, SE2010.E2_EMISSAO, 103)		AS DT_EMISSAO,
	CONVERT(DATE, SE2010.E2_VENCTO, 103)		AS DT_VENCIMENTO,
	ISNULL(NULLIF(E2_XDOC,''),0) 						AS NUMERO,
	SE2010.E2_VALOR												AS VALOR,
	E2_HIST																AS HISTORICO,
	'' 																			AS OBS2,
	'' 																			AS OBS3,
	SE2010.E2_FILORIG												AS FILIAL_TOTVS,
	'?'																			AS CANCELADO
FROM 
	SE2010 
	INNER JOIN ZZ1010 ON 
	SE2010.E2_FILORIG = ZZ1010.ZZ1_FILPRO
WHERE 
	SE2010.D_E_L_E_T_ = '' 
	AND ZZ1010.D_E_L_E_T_ = ''
	AND SE2010.E2_TIPO = 'NDF'
	